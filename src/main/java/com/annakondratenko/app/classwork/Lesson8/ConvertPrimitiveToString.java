public class ConvertPrimitiveToString {
    public String convertPrimitiveDoubleToString(double val) {
        String display = String.valueOf(val);
        System.out.println(display);
        return display;
    }

    public String convertPrimitiveIntToString(int val) {
        String display = String.valueOf(val);
        System.out.println(display);
        return display;
    }

    public String convertPrimitiveLongToString(long val) {
        String display = String.valueOf(val);
        System.out.println(display);
        return display;
    }

    public String convertPrimitiveBooleanToString(boolean val) {
        String display = String.valueOf(val);
        System.out.println(display);
        return display;
    }
}

